"""
Responsible-AI runner — golden-set judge + red-team + fairness audit → report.

Run:  python -m eval.responsible_ai        (from the backend/ directory)
Writes evaluation/responsible_ai.md and returns a summary. All $0 / offline.
"""
from __future__ import annotations

import json
import os
import sys
from pathlib import Path
from statistics import mean
from typing import Any, Dict

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from app.agents import chat_agent, orchestrator  # noqa: E402
from app.config import settings  # noqa: E402
from app.tools import audit, db  # noqa: E402
from eval import fairness, judge, redteam  # noqa: E402

_GOLDEN = Path(__file__).resolve().parent.parent.parent / "evaluation" / "golden_set.json"


def _golden_eval() -> Dict[str, Any]:
    items = json.loads(_GOLDEN.read_text())["items"]
    scores, fails = [], []
    for it in items:
        result = orchestrator.run_case(it["case_id"])
        ans = chat_agent.answer(result, it["case_id"], it["question"])
        verdict = judge.deterministic_judge(ans["answer"], it["expected_contains"])
        scores.append(verdict["grounded"])
        if verdict["verdict"] == "fail":
            fails.append({"case_id": it["case_id"], "q": it["question"]})
    return {"n": len(items), "avg_groundedness": round(mean(scores), 3) if scores else 0.0,
            "pass_rate": round(sum(1 for s in scores if s >= 0.5) / max(len(scores), 1), 3),
            "failures": fails}


def _fairness_eval() -> Dict[str, Any]:
    records = []
    for c in db.list_cases():
        r = orchestrator.run_case(c["case_id"])
        records.append({"group": r["evidence"]["subject_kyc"].get("residence_country"),
                        "risk": r.get("risk", {}).get("overall_risk", 0.0)})
    return fairness.audit(records)


def run() -> Dict[str, Any]:
    audit.init_db()
    golden = _golden_eval()
    rt = redteam.run_redteam(orchestrator.run_case("CASE-0001"), "CASE-0001")
    fair = _fairness_eval()
    summary = {"golden": golden, "redteam": rt, "fairness": fair,
               "llm_judge_available": settings.llm_provider != "offline"}
    _write(summary)
    return summary


def _write(s: Dict[str, Any]) -> None:
    out = Path(__file__).resolve().parent.parent.parent / "evaluation" / "responsible_ai.md"
    g, rt, f = s["golden"], s["redteam"], s["fairness"]
    rt_rows = "\n".join(f"| {o['name']} | {o['category']} | {o['expect']} | "
                        f"{'blocked' if o['blocked'] else 'answered'} | "
                        f"{'✅' if o['passed'] else '❌'} |" for o in rt["outcomes"])
    fair_rows = "\n".join(f"| {g_} | {v['n']} | {v['mean_risk']} | {v['flag_rate']} |"
                          for g_, v in f["groups"].items())
    md = f"""# Responsible-AI Report — ComplianceAgent

> Generated by `python -m eval.responsible_ai`. Deterministic, $0. An optional
> LLM-as-judge (`eval/judge.py`) runs when a Gemini key is set.

## 1. Golden-set groundedness (analyst chat)
Human-curated Q&A ([`evaluation/golden_set.json`](golden_set.json)); the deterministic
judge checks each answer contains the required facts.

- Items: **{g['n']}** · avg groundedness **{g['avg_groundedness']}** · pass rate **{g['pass_rate']}**
- Failures: {g['failures'] or 'none'}

## 2. Red-team / jailbreak suite
Adversarial prompts against the chat agent — each must be blocked or answered safely.

| Attack | OWASP | Expect | Result | Pass |
|---|---|---|---|---|
{rt_rows}

**Pass rate: {rt['pass_rate']} ({rt['passed']}/{rt['total']})**

## 3. Bias / fairness audit
Group-fairness of risk flagging by **{f['sensitive_attribute']}**.

| Group | n | mean risk | flag rate |
|---|---|---|---|
{fair_rows}

- Demographic-parity difference: **{f['demographic_parity_difference']}**
- Disparate-impact ratio: **{f['disparate_impact_ratio']}** ({f['rule']})
- Concern flag: **{'⚠️ yes' if f['concern_flag'] else 'no'}**

> {f['caveat']}

See also the system [Model Card](../docs/MODEL_CARD.md) and [Data Sheet](../docs/DATA_SHEET.md).
"""
    out.write_text(md)


if __name__ == "__main__":
    s = run()
    print("=== Responsible-AI ===")
    print(f"golden groundedness: {s['golden']['avg_groundedness']} "
          f"(pass {s['golden']['pass_rate']})")
    print(f"redteam pass rate:   {s['redteam']['pass_rate']} "
          f"({s['redteam']['passed']}/{s['redteam']['total']})")
    print(f"fairness DI ratio:   {s['fairness']['disparate_impact_ratio']} "
          f"(concern={s['fairness']['concern_flag']})")
